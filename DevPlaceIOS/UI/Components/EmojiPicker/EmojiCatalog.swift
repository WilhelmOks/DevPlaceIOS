    static func searchName(for emoji: String) -> String {
        emoji.unicodeScalars
            .compactMap { $0.properties.name }
            .joined(separator: " ")
            .lowercased()
    }
    
    static func emojis(matching query: String) -> [String] {
        let normalized = query.lowercased().trimmingCharacters(in: .whitespaces)
        guard !normalized.isEmpty else { return [] }
        return categories
            .flatMap(\.emojis)
            .filter { searchName(for: $0).contains(normalized) }
    }
}

extension Array {
    func chunked(into size: Int) -> [[Element]] {
        guard size > 0 else { return [self] }
        return stride(from: 0, to: count, by: size).map {
            Array(self[$0 ..< Swift.min($0 + size, count)])
        }
    }
}
